    function deleteConfirmation(model, id) {
        swal({
            title: "Delete?",
            text: "Please ensure and then confirm!",
            type: "warning",
            showCancelButton: 1,
            confirmButtonText: "Yes, delete it!",
            cancelButtonText: "No, cancel!",
            reverseButtons: !0
        }).then(function(e) {
            if (e.value === true) {
                const CSRF_TOKEN = $('meta[name="csrf-token"]').attr('content');
                $.ajax({
                    type: 'DELETE',
                    url: `/${model}/${id}`,
                    data: {
                        _token: CSRF_TOKEN
                    },
                    dataType: 'JSON',
                    success: function(results) {
                        if (results.code === 200) {
                            setTimeout(function() {
                                location.reload();
                            }, 1000);
                            swal("Done!", "", "success");
                        } else {
                            swal("Error!", "", "error");
                        }
                    },
                    error: function(err) {
                        swal("Error!", "There was an error, please try again later", "error");
                    }
                });
            } else {
                e.dismiss;
            }
        }, function(dismiss) {
            return false;
        })
    }
</script>
